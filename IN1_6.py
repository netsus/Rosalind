'''
문제 : 주어진 한 문장에대해 공백을 구분자로하여 단어의 중복된 개수 출력하기
알고리즘 : 딕셔너리 이용 단어를 순차적으로 탐색하여 딕셔너리에 없으면 추가, 있으면 1더하기'''
st = input().split()

di = dict()
for i in st:
    if i not in di:
        di[i]=1
    else:
        di[i]+=1

for k,v in di.items():
    print(k,v)

