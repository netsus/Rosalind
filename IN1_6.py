'''
문제 : 두 양수 주어지고, 양수사이의 모든 홀수의 합 구하기
알고리즘1 : for문 이용하여 두 양수사이의 2로 나누어서 0 안되는 수의 합
알고리즘2 : 리스트컴프리핸션 이용 마찬가지
'''
start,end = map(int,input().split())
total=0
for i in range(start,end+1):
    if i%2 != 0:
        total+=i
print(total)

